	  <h1 id="about">What We Do</h1><br>
		<b>Waste Exchange System</b> is an initiative to save the world from pollution. It will not only make waste materials collection more organized but
		also motivate people to collect and store their wastes rather than throwing them. Our system
		helps to collect wastes like e-waste, plastics, papers, and rewards the waste provider according to the
		amount of waste. The wastes are then delivered to the waste-collecting companies. Since waste
		providers will be rewarded with goods in exchange for wastes they will be more enthusiastic to
		store their waste and benefit from them rather than throwing those random places.<br>
		<b>Waste Exchange system</b> will provide a platform between the normal people and the manufacturing
        or recycling companies. The companies can collect their raw materials like used plastics, poly
		bags, papers, glass and e-waste from people. People can choose the
		type and quantity of the items. After confirming, a worker from recycling company
		will come to the given location to collect them. According to the quantity of the items, people
		can receive money or offered goods by the companies. This will help to keep hazardless
